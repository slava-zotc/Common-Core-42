#include "libft.h"

int	ft_isalnum(int ch)
{
	if ((ch >= 'A' && ch <= 'Z') || (ch >= 'a' && ch <= 'z')
		|| (ch <= '9' && ch >= '0'))
		return (1);
	else
		return (0);
}
