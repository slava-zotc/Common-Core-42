#include "libft.h"

int	ft_print_str(const char *s)
{
	int		i;
	int		res;

	i = 0;
	res = 0;
	if (!s)
	{
		return (write(1, "(null)", 6));
	}
	else
	{
		while (s[i])
		{
			res = write(1, &s[i], 1);
			if (res == -1)
				return (-1);
			i++;
		}
	}
	return (i);
}
